def get_plants_by_sunlight(hours: float) -> list:
    """Get all plants suitable for given sunlight hours."""
    suitable = []
    for plant_key, plant in PLANTS.items():
        if plant["sun_hours_min"] <= hours:
            suitable.append(plant_key)
    return suitable

def get_plants_by_zone(zone: str) -> list:
    """Get plants suitable for a USDA zone based on growing days."""
    if zone not in USDA_ZONES:
        return []
    growing_days = USDA_ZONES[zone]["growing_days"]
    suitable = []
    for plant_key, plant in PLANTS.items():
        if plant["days_to_maturity"] <= growing_days:
            suitable.append(plant_key)
    return suitable

def get_shade_tolerant_plants() -> list:
    """Get plants that tolerate shade (4 or fewer sun hours)."""
    return [p for p, data in PLANTS.items() if data["sun_hours_min"] <= 4]

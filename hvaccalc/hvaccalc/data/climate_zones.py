def get_closest_r_factor(r_value: float) -> dict:
    """Return the closest R-value factor entry."""
    available = sorted(R_VALUE_FACTORS.keys())
    closest = min(available, key=lambda x: abs(x - r_value))
    return R_VALUE_FACTORS[closest]


